var oneisGray = true;
console.log(oneisGray);
$('.firstbox').on('click', function() {
	if (oneisGray == true) {
  	$('.firstbox').css('background','none');
  	oneisGray = false;
  } else { 
  	$('.firstbox').css('background','gray');
    oneisGray = true;
  }
  console.log(oneisGray);
})
